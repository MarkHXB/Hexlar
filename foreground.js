class CoinValidator {
    constructor(_url, _bodyText) {
        this.url = url
        this.bodyText = _bodyText

        this.coin = this._SearchInUrl() || this._SearchInBody()
    }

    IsValid() {
        return (this.coin !== undefined && this.coin !== "")
            ? VALID_COINS.includes(this.coin)
            : false
    }

    _SearchInUrl() {
        let _coin = ""

        VALID_COINS.map((a_coin) => {
            if (this.url.includes(a_coin)) {
                _coin = a_coin
            }
        })

        return _coin;
    }

    _SearchInBody() {
        let _coin = ""

        VALID_COINS.map((v_coin) => {
            if (this.bodyText.includes(v_coin)) {
                _coin = v_coin;
            }
        })

        return _coin
    }
}

